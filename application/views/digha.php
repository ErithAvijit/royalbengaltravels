<p>Tajpur
Located in Purba Medinipur, West Bengal, India on the shore of Bay of Bengal (near Digha) Tajpur is one of the new additions in tourist map of Bengal. The prime attraction of Tajpur is its pristine sea beach fringed with a dense forest of tamarisk trees. The beach is infested with infinite number of red crabs which are great to watch when they are in numbers. There are fishermen's villages in the vicinity, where one can observe the mundane life of fisherman.</p>
